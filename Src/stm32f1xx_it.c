/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
	if (start)
	{
		switch (tick++) 
		{
		case 0:
			// task 0 here
			break;
		case 1:
			// task 1 here
			break;
			// and so on
			case 999 :
			    tick = 0;
			second = (second < 59) ? (second + 1) : 0;
			minute = (second == 0) ? ((minute < 59) ? (minute + 1) : 0) : minute;
			minute_global = minute;
			second_global = second;
			break; 
		}
	}
	else
	{
		second = 0;
	}
	
  /* USER CODE END TIM3_IRQn 1 */
}

/**
  * @brief This function handles EXTI line[15:10] interrupts.
  */
void EXTI15_10_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI15_10_IRQn 0 */

  /* USER CODE END EXTI15_10_IRQn 0 */
  if (LL_EXTI_IsActiveFlag_0_31(LL_EXTI_LINE_12) != RESET)
  {
    LL_EXTI_ClearFlag_0_31(LL_EXTI_LINE_12);
    /* USER CODE BEGIN LL_EXTI_LINE_12 */
		if(!start)
		{
			setTime++;
		}
    /* USER CODE END LL_EXTI_LINE_12 */
  }
  if (LL_EXTI_IsActiveFlag_0_31(LL_EXTI_LINE_13) != RESET)
  {
    LL_EXTI_ClearFlag_0_31(LL_EXTI_LINE_13);
    /* USER CODE BEGIN LL_EXTI_LINE_13 */
		if(!start)
		{
			setTime--;
			if(setTime<=0)
			{
				setTime = 0;
			}
		}

    /* USER CODE END LL_EXTI_LINE_13 */
  }
  if (LL_EXTI_IsActiveFlag_0_31(LL_EXTI_LINE_14) != RESET)
  {
    LL_EXTI_ClearFlag_0_31(LL_EXTI_LINE_14);
    /* USER CODE BEGIN LL_EXTI_LINE_14 */
		if(!start)
		{
			SET_BIT(TIM2->CCER, TIM_CCER_CC3E);
			start = true;
		}
		while(start && !READ_BIT(GPIOB->IDR, GPIO_IDR_IDR13))
		{
			start = false;
			CLEAR_BIT(TIM2->CCER, TIM_CCER_CC3E);
		}
    /* USER CODE END LL_EXTI_LINE_14 */
  }
  /* USER CODE BEGIN EXTI15_10_IRQn 1 */

  /* USER CODE END EXTI15_10_IRQn 1 */
}

